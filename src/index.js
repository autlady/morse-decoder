function decode(expr) {
    let res = "";
    let array = Array.from(expr);
    console.log(array);
    for (let index = 0; index < array.length; ++index) {
        if (array[index] === " ") {
            res = res + "******";
        } else {
            res = res + Object.keys(MORSE_TABLE).find(key =>
                MORSE_TABLE[key] === array[index]);
        } 
    }
    return res;
}

